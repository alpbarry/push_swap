#include "push_swap.h"

long	input_is_correct(char *s)
{
	int	i;

	i = 0;
	if ((s[i] == '-' || s[i] == '+') && (ft_strlen(s) > 1))
			i++;
	while (s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
